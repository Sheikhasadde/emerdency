                <h2>2. Dental Veneers</h2>

 <img class="in-text-img" src="images/blog/effective-tooth%20(4).JPG" alt="Dental veneers"/>
                </div>

                <p>Veneers are very thin prosthetic restorations that adhere directly to the visible surface of your tooth.  They are a great cosmetic solution to minor aesthetic concerns. </p>
                <p>There are two main types of veneers:  </p>

                <ol type="a">
                  <li>Composite Veneers <br/>
                    <p>Composite veneers are a direct restoration which means they are made by the dentist in the dental clinic.  The advantages of these veneers is they can be provided with fewer appointments and therefore more cost effective.  A further advantage is the ease of repair if the restoration gets damaged. </p>
                  </li>
                  <li>Porcelain Veneers  <br/>
                    <p>Porcelain veneers are an indirect restoration and therefore these are constructed in a dental lab by a skilled lab technician.  This procedure requires two appointments.  The first appointment is for the preparation of the tooth, which is then followed by the cementation of the veneer.     </p>
                    <p>If done correctly veneers are a great option allowing you to achieve superior aesthetic results with minimal preparation of healthy tooth tissue.   </p>
              </ol>

                <h2>3. Dental Crown </h2>

<img class="in-text-img" src="images/blog/effective-tooth%20(5).JPG" height="500" width="100%" alt="Dental Crown"/>

              <p>When a tooth has fractured significantly and there is not much tooth structure, a crown would be the ideal option. A crown, also known as a cap, is a fixed prosthetic restoration used to restore a weakened tooth re-establishing its natural shape.</p>
              <p>Crowns are indirect restorations, which are also manufactured in a lab by a lab technician.  Two appointments are required for this type of restoration.  During the first appointment, the tooth is prepared and then during the follow up the crown is cemented.   </p>

              There are a few different types of crown available to you. The most common crowns are listed below: <br/>

              <ol>
                <li>Metal alloy crowns <br/>
                  <p>These crowns are made from a combination of metals. This gives the crown the advantage of strength, durability, corrosion resistant and most importantly, minimal tooth tissue loss. Hence, one of the most conservative crown options.  However, It has its drawback, the metallic colour is not pleasing to the eye and  therefore the least favourable aesthetic option.</p>
                </li>
                <li>Porcelain fused to metal <br/>
                    <p>A crown which has a metal substructure and an outer layer of white porcelain.  These crowns are tooth coloured and therefore a good aesthetic option.</p>
                </li>
                  <li>All ceramic crowns
                      <p>These modern crowns are made from a stronger porcelain based material. The metal free nature of all ceramic crowns gives you the best of both worlds, strength and aesthetically pleasing to the eye.</p>
                      <p>In the event of an emergency a temporary crown can be made using preformed temporary crowns.  </p>
                  </li>
              </ol>

                
                <h2>4. Root Canal Treatment</h2>

<img class="in-text-img" src="images/blog/effective-tooth%20(2).JPG" height="500" width="100%" alt="Root canal diagram"/>

                <p>In the event your tooth has broken or decayed extending to the pulp of the tooth, then you will require root canal treatment.  The pulp chamber of the tooth houses the nerve and blood supply of the tooth.  When this area is compromised with the ingress of bacteria this can lead to symptoms requiring action.  </p>
                <p>During root canal treatment, the infected/necrotic pulp tissue will be removed.  Following this, the root canal system will be disinfected using an antibacterial irrigant and shaped using endodontic files.  Once copiously disinfected and cleaned, the root canals will be filled with a rubber based material known as Gutta-percha.   </p>
                <p>Once the canals are sealed, the dentist will build up a structure where a dental cap or crown may be required.   As per guidelines it is recommended to have back teeth crowned following root canal treatment in order to provide cuspal protection and prevent vertical fractures.  </p>

                <h2>5. Extraction</h2>
                <p>If a tooth breaks in such a manner that it extends beyond the gum line, it may not be possible to restore the tooth. In such cases the tooth is extracted.  Once extracted there are a number of options to replace the gap.</p>

