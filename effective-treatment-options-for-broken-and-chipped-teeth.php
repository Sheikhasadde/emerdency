                <p>The frequency of chipped and broken teeth among dental patients have been increasing by the day. While the growing trend is rather peculiar, there is no denying that this matter is not to be ignored.</p>
                <p>A chipped tooth not only can be an aesthetic concern but it could indicate a deeper underlying problem within the tooth.  You may feel that a chipped tooth cannot be restored, but rest assured there are various treatment options available to help restore your smile.   </p>
                <p>If you have suffered from a chipped tooth, have a read of the treatment options available to you.  </p>

                <h2>1. Tooth Bonding</h2>

 <img class="blog-hero" src="images/blog/effective-tooth%20(1).JPG" alt="Before and after dental treatment"/>

                <p>In cosmetic dentistry tooth bonding is a procedure used to fix a chipped, broken or congenitally malformed tooth. This is a treatment option that is very cost effective and minimally invasive as it maintains healthy tooth tissue.   </p>
                <p>It is also called composite bonding. The dentist will apply a composite material on the
                    damaged part of the tooth, the composite material is shaped and carved to mimic your
                    natural tooth. Once the desired appearance is achieved the composite is set with a blue
                    light.  This is followed by a systematic process of polishing and shaping in order to give  the
                    new filling a natural appearance. Various shades are available allowing the dentist to match
                    a shade that compliments your natural teeth giving you great aesthetic results. </p>
                <p>If done correctly it can last for quite some time, anywhere between 5-10 years.  </p>

